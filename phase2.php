    	This document is a very early draft of potential ideas for phase II. Some of these ideas may be incorporated into phase I, as phase I plans become finalized, especially if it is necessary in order to easily to implement phase II objectives.
    	<h1>TRC-1155</h1>
    	Right now the only token standards on Tron are TRC-10 and TRC-20. TRC-10 is great because it's a natively supported token that's easy to create and use. TRC-20 has all the issues of ERC-20 tokens, including a lack of lost funds protection. Something that we would like to see is to take ERC-1155 standards and bring them to Tron. ERC-1155 is currently one of the most interesting token standards we've seen, allowing both fungible and non-fungible tokens, cross game assets, melting of assets to receive something of general value, and more.
    	<h1>Super Representative Status</h1>
    	One of our concerns is that almost all the super representatives are managed by the TRON Foundation. If a single institution has almost full control over the network, it's not truly decentralized. If World Builder, as a group, could become a Super Representative, it would make a big difference wrt taking some control away from the TRON Foundation itself. However, the true goal is to delegate this level of control, and so each municipality will have a vote as to how we as a SR would vote.
